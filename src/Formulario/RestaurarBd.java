    public void respaldo() {
        
        ConexionBD.conectar();
         barra();
        String ruta = txtruta.getText();
//        String nombrescrip = "\\tienda.sql";
        String backup = "";
        //Metodo para ejecutar la restauracion de la base de datos
        if (ruta.trim().length() != 0) {
                          
           
            try {
                  
                Process p = Runtime.getRuntime().exec("mysql -u root -pmora2017 tienda");
                OutputStream os = p.getOutputStream();
                FileInputStream fis = new FileInputStream(ruta);
                byte[] buffer = new byte[1000];
                int leido = fis.read(buffer);
                while (leido > 0) {
                    os.write(buffer, 0, leido);
                    leido = fis.read(buffer);

                }
                os.flush();
                 
                os.close();
            
                fis.close();
               
                
                   
                
                JOptionPane.showMessageDialog(null, "Base de datos restaurada");
                
                this.dispose();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Error al restaurar" + e);
            }
        }
        
    }
