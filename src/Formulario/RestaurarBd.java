    public void buscar() {
        JFileChooser archivos = new JFileChooser();
        FileNameExtensionFilter flitro = new FileNameExtensionFilter("SQL", "sql");
        archivos.setFileFilter(flitro);
        int se = archivos.showOpenDialog(null);
        if (se == JFileChooser.APPROVE_OPTION) {
            String ruta = archivos.getSelectedFile().getPath();
            txtruta.setText(ruta);

        }

    }


